    private void btnSignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignupActionPerformed
        // TODO add your handling code here:
        JFrame animationFrame = new JFrame();
animationFrame.setUndecorated(true); // removes borders
animationFrame.setSize(1000, 800);
animationFrame.setLocationRelativeTo(this);

JLabel gifLabel = new JLabel(new ImageIcon(getClass().getResource("/Images/geunyeong-park-book.gif")));
animationFrame.setContentPane(gifLabel);

// Show the animation window
animationFrame.setVisible(true);

Timer timer = new javax.swing.Timer(2000, e -> {
    animationFrame.dispose();           // close the animation window
    new Signup().setVisible(true);      // open your registration frame
     this.dispose(); // 'this' = Login frame
});
timer.setRepeats(false);                // <- fire only once
timer.start();

        
    }//GEN-LAST:event_btnSignupActionPerformed

    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String username = txtUsername.getText().trim();
String password = txtPassword.getText().trim();

if (UserData.validateUser(username, password)) {
    JOptionPane.showMessageDialog(this, "Login successful!");
    this.dispose();
    new Menu().setVisible(true);
    
} else {
    JOptionPane.showMessageDialog(this, "Invalid username or password!");
}

        
    }//GEN-LAST:event_btnLoginActionPerformed

